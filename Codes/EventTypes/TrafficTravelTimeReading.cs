using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace EventTypes
{
    public class TrafficTravelTimeReading
    {
        public TrafficTravelTimeReading()
        {
        }
        /*  For each item
            •	travel link ID 
            •	speed
            •	Travel time
         */

        public TrafficTravelTimeReading(int ID, double sp, double time)
        {
            travelId = ID;
            speed = sp;
            travelTime = time;
        }

        public int travelId { get; set; }
        public double speed { get; set; }
        public double travelTime { get; set; }
        
    }
    
}
